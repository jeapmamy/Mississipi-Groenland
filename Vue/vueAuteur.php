			en 1999 joue avec Chantal Lauby dans le sitcom <strong>Evamag.</strong></p>

			<p>En 2003, il joue dans le film <strong>L'Homme de la riviera</strong> avec Nick Nolte, Gérard Darmon et Tchéky Karyo. 

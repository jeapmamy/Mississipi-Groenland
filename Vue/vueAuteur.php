			<p>Jean Forteroche publie son premier roman, <strong>Les Hannetons</strong>, en mars 1991.<br /> 
			Ses œuvres ont été traduites en trente-cinq langues. Avec 20 millions d’exemplaires vendus dans le monde.</p>
			

			<p>À la suite de son dernier livre <strong>Le refuge des Antilopes</strong>, il a lancé son propre blog.</p>

			<p>Pour son nouveau roman <strong>Billet simple pour l'Alaska</strong>, il souhaite innover et le publier 
			par épisode, en ligne sur son propre site.</p>
			

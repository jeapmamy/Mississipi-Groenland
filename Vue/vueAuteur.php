		<legend id="titreReponses">Ecriture</legend>
		<div class="bandeau">
			<p>Son style d’écriture mêle différents genres, notamment la saga d’aventure, 
			la science-fiction, l’entre-deux-guerres et le conte philosophique.</p>

			<p>Dans la plupart de ses romans, Jean Forteroche utilise la même forme de construction, 
			alternant des articles informatifs d’encyclopédie et deux ou trois fils narratifs. 
			Les articles précisent ou élargissent les intrigues, ces deux dernières se recoupant épisodiquement.</p>

			<p>De plus, toutes ses productions, par exemple ses romans et ses nouvelles, s’entrecroisent. 
			On retrouve ainsi des personnages à la fois dans <strong>Les Hannetons</strong> et <strong>Le refuge des Antilopes</strong>.</p>		

			<p>Dans deux romans, Jean Forteroche décrit des auteurs de littérature. 
			Ainsi sont présentés des auteurs qui, à la fin de leur vie, écrivent un dernier livre 
			qui donne un sens à l’œuvre entière.</p>
			
